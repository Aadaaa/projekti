  <h1> Festa e mjaltit </h1>
  <h3>Në Librazhd organizohet “Festa e Mjaltit”, 100 biznese promovojnë prodhimet e tyre</h3>
  <p>
    Bashkia e Librazhdit në bashkëpunim me  World Vision  dhe CNVP  organizuan  gjatë kësaj fundjave në  pedonalen e rivitalizuar të qytetit të Librazhdit për herë të parë  aktivitetin  e titulluar “Festa e Mjaltit”  në të cilën  morën pjesë  rreth 100  biznese të cilat promovuan  prodhime të  ndryshme nga më cilësoret.
Bletaria në zonën e Librazhdit  është   një traditë e hershme dhe  Mjalti I kësaj zone   është nga më të preferuarit  jo vetëm në tregun vendas por sidomos në tregun  e huaj .
Mjalti i geshtenjes,i luleve i pishes,listres etj,jane disa variete mjalti qe gjenden vetem në rrethin e  Librazhdit.Sipas statistikave më të fundit actual;isht ka me shumë se 153 blete-rritës në të gjithe  territorin e Bashkisë së  Librazhdit. Bazuar edhe ne programet kombetare, Bashkia Librazhd sipas kryebashkiakut Kastriot Gurra po përkushtohet tju japi vëmendjen e duhur dhe ti mbështesi këto biznese të vogla me anë të  politikave lokale  në mënyrë që  numri i tyre të vije gjithmonë  në rritje, gjë  që do ndikojë në rritjen e mirëqenies të familjeve librazhdase.<br>
Data: 30/10/2020<br>
